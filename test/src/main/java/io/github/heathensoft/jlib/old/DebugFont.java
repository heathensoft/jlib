    private final Texture font_texture;
    private final TextureRegion[] characters;
    
    private static final record Glyph(char c, float x, float y,
    float advance, float lsb, float rsb, float decent, float ascent) { }
    
    public DebugFont() {
        font_texture = font_rgba8();
        TextureRegion font_region = new TextureRegion(font_texture,0,0,font_img_w,font_img_h);
        font_region.subDivide(font_img_cols,font_img_rows);
        characters = new TextureRegion[font_characters];
        for (int i = 0; i < font_characters; i++) {
            characters[i] = font_region.subRegion(i);
        }
    }
    
    public TextureRegion get(char c) {
        return characters[Math.max(0,(c & 0x7F)-32)];
    }
    
    public TextureRegion get(byte c) {
        return characters[Math.max(0,(c & 0x7F)-32)];
    }
    
    public Texture font_atlas() {
        return font_texture;
    }
    
    public float advance() {
        return font_width;
    }
    
    public float leading() {
        return font_height;
    }
    
    private Texture font_rgba8() {
        Texture texture = null;
        try { Image img = new Resources(DebugFont.class).
            image("res/jlib/lwjgl/font/debug.png");
            texture = new Texture(GL_TEXTURE_2D);
            texture.bindToActiveSlot();
            texture.wrapST(GL_CLAMP_TO_BORDER);
            texture.linear();
            texture.image_2D(img);
        } catch (Exception e) {
            Logger.error(e,"unable to load debug font");
            Disposable.dispose(texture);
            texture = new Texture(GL_TEXTURE_2D);
            texture.bindToActiveSlot();
            texture.wrapST(GL_CLAMP_TO_BORDER);
            texture.nearest();
            IntArray2D array = new IntArray2D(font_img_h,font_img_w);
            array.write(0xFFFFFFFF);
            texture.RGBA8_2D(array);
        } return texture;
    }
    
    
    private byte[] toBytes(long[] src) {
        int words = src.length;
        if (words == 0) return new byte[0];
        byte[] result = new byte[words * 8];
        for (int i = 0; i < words; i++) {
            long word = src[i];
            int offset = i * 8;
            result[7+offset] = (byte) ((word >> 56) & 0xff);
            result[6+offset] = (byte) ((word >> 48) & 0xff);
            result[5+offset] = (byte) ((word >> 40) & 0xff);
            result[4+offset] = (byte) ((word >> 32) & 0xff);
            result[3+offset] = (byte) ((word >> 24) & 0xff);
            result[2+offset] = (byte) ((word >> 16) & 0xff);
            result[1+offset] = (byte) ((word >> 8) & 0xff);
            result[ +offset] = (byte) ((word & 0xff));
        } return result;
    }
    
